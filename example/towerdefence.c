void action(){
	key = getkey();
	if(key != prevkey){
		if(key == KEY_LEFT){
			if(x > 0)
				x--;
		}
		else if(key == KEY_RIGHT){
			if(x < 7)
				x++;
		}
		if(key == KEY_UP){
			if(y > 0)
				y--;
		}
		else if(key == KEY_DOWN){
			if(y < 6)
				y++;
		}
		else if(key == KEY_A){
			if(istower(x + y * 8) & 1){
				updatetower();
			}
			else{
				newtower();
			}
		}
		else if(key == KEY_B){
			putimage(scursor, 5 + mpos * 25, 113, 7, 4);
			mpos++;
			if(mpos >= 3)
				mpos = 0;
			putimage(mcursor, 5 + mpos * 25, 113, 7, 4);
		}
		if(istower(x + y * 8) & 1)
			getsprite(0, cursorup);
		else if(istower(x + y * 8))
			getsprite(0, cursoroff);
		else
			getsprite(0, cursor);
		putsprite(0, x * 16, y * 16);
	}
	prevkey = key;
}

