int tan(int angle, int dist){
	angle = angle % 360;
	if(angle < 90)
		return ((tangtable[angle] * dist) >> 8);
	else if(angle < 180)
		return (0 - ((tangtable[180 - angle] * dist) >> 8));
	else if(angle < 270)
		return ((tangtable[angle - 180] * dist ) >> 8);
	else if(angle < 360)
		return (0 - ((tangtable[360 - angle] * dist) >>8));
}

